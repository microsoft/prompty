"""Message types and streaming wrappers for model-agnostic prompt representation.

The canonical type definitions (``Message``, ``ContentPart``, ``TextPart``, etc.)
are generated by the TypeSpec emitter and live in ``prompty.model``. This module
re-exports them and adds convenience methods (``Message.text``,
``Message.to_text_content``) plus runtime-only types (``PromptyStream``,
``AsyncPromptyStream``, constants).

The parser produces ``Message`` objects. Executors transform them into
provider-specific wire format (OpenAI, Anthropic, etc.).
"""

from __future__ import annotations

from collections.abc import AsyncIterator, Iterator
from typing import Any

# Re-export generated types — these are the canonical definitions.
from ..model import (
    AudioPart,
    ContentPart,
    FilePart,
    ImagePart,
    Message,
    TextPart,
    ThreadMarker,
)

__all__ = [
    "AsyncPromptyStream",
    "AudioPart",
    "ContentPart",
    "FilePart",
    "ImagePart",
    "Message",
    "PromptyStream",
    "TextPart",
    "ThreadMarker",
    "RICH_KINDS",
    "ROLES",
]

# Rich input kinds that the renderer handles structurally (not as text).
RICH_KINDS = frozenset({"thread", "image", "file", "audio"})

# Supported role markers that the parser recognizes.
ROLES = frozenset({"system", "user", "assistant", "developer"})


# ---------------------------------------------------------------------------
# Convenience methods added to generated Message
# ---------------------------------------------------------------------------


def _message_text(self: Message) -> str:
    """Convenience: concatenate all TextPart values."""
    return "".join(p.value for p in self.parts if isinstance(p, TextPart))


def _message_to_text_content(self: Message) -> str | list[dict[str, Any]]:
    """Return plain string if all parts are text, else a parts list.

    This is useful for simple serialization — callers that only need
    the text content can get a ``str``, while multimodal messages
    get the full parts array.
    """
    if all(isinstance(p, TextPart) for p in self.parts):
        return _message_text(self)
    return [_part_to_dict(p) for p in self.parts]


# Augment the generated Message class with convenience accessors.
Message.text = property(_message_text)  # type: ignore[attr-defined]
Message.to_text_content = _message_to_text_content  # type: ignore[attr-defined]


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------


def _part_to_dict(part: ContentPart) -> dict[str, Any]:
    """Convert a ContentPart to a plain dict."""
    if isinstance(part, TextPart):
        return {"kind": "text", "value": part.value}
    elif isinstance(part, ImagePart):
        d: dict[str, Any] = {"kind": "image", "source": part.source}
        if part.detail is not None:
            d["detail"] = part.detail
        if part.media_type is not None:
            d["mediaType"] = part.media_type
        return d
    elif isinstance(part, FilePart):
        d = {"kind": "file", "source": part.source}
        if part.media_type is not None:
            d["mediaType"] = part.media_type
        return d
    elif isinstance(part, AudioPart):
        d = {"kind": "audio", "source": part.source}
        if part.media_type is not None:
            d["mediaType"] = part.media_type
        return d
    else:
        return {"kind": part.kind}


