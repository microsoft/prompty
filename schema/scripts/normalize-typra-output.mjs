import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const metadataRoot = join("tsp-output", ".typra-generated");
const manifestPath = join(metadataRoot, "manifest.json");

if (existsSync(manifestPath)) {
  const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
  manifest.generatedAt = "<normalized>";
  writeFileSync(manifestPath, `${JSON.stringify(manifest, null, 2)}\n`);
}

trimEmptyPythonGeneratedTests(join("..", "runtime", "python", "prompty", "tests", "model"));
trimTrailingWhitespace(join("..", "runtime", "go", "prompty", "model"));

function trimEmptyPythonGeneratedTests(root) {
  if (!existsSync(root)) {
    return;
  }
  for (const entry of readdirSync(root)) {
    const path = join(root, entry);
    if (statSync(path).isDirectory()) {
      trimEmptyPythonGeneratedTests(path);
      continue;
    }
    if (!path.endsWith(".py")) {
      continue;
    }
    const content = readFileSync(path, "utf8");
    if (content.trim() === "# <auto-generated by typra-emitter>") {
      writeFileSync(path, "# <auto-generated by typra-emitter>\n");
    }
  }
}

function trimTrailingWhitespace(root) {
  if (!existsSync(root)) {
    return;
  }
  for (const entry of readdirSync(root)) {
    const path = join(root, entry);
    if (statSync(path).isDirectory()) {
      trimTrailingWhitespace(path);
      continue;
    }
    if (!path.endsWith(".go")) {
      continue;
    }
    const content = readFileSync(path, "utf8");
    const normalized = content
      .split("\n")
      .map((line) => line.replace(/[ \t]+$/u, ""))
      .join("\n");
    if (normalized !== content) {
      writeFileSync(path, normalized);
    }
  }
}
